import math

pi = 3.14
x = 2
y = 3
z = 4

print("Rounded : " + str(round(pi)))
print("Ceiling : " + str(math.ceil(pi)))
print("Floor : " + str(math.floor(pi)))
print("Absolute : " + str(abs(pi)))
print("Power of : " + str(pow(pi, 2)))
print("Square root : " + str(math.sqrt(pi)))
print("Max value : " + str(max(x, y, z)))
print("Min value : " + str(min(x, y, z)))