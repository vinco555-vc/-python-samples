
name = "Vinnie"

#length
print(len(name))

#Find
print(name.find("n"))

#Capitilze
print(name.capitalize())
#Upper
print(name.upper())
#Lower
print(name.lower())
#IsDigit
print(name.isdigit())
#isAlpha
print(name.isalpha())
#count
print(name.count("n"))
#replace
print(name.replace("n", "w"))

#Display Multiple times
print(name*3)
